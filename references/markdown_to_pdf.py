#!/usr/bin/env python3
"""Convert a Markdown document to a readable PDF.

The script is intentionally dependency-light and reusable for future skill
deliverables. It focuses on Chinese-friendly resume/project reports generated by
this repository: headings, blockquotes, bullets, ordered lists, tables, links,
inline code, bold text and code blocks.

Dependency:
    python -m pip install reportlab

Usage:
    python jdhunter/references/markdown_to_pdf.py \
        jd-match-resume-pack.md

    python jdhunter/references/markdown_to_pdf.py \
        input.md --output output.pdf --title "JD 匹配项目推荐报告"
"""

